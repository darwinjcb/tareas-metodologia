'use strict'
// Forma tradicional (mas común):
function suma(a, b) {
    return a + b;
}
// Expresión de función:
let sumaExp = function (a, b) {
    return a + b;
}

// Funcion 'Flecha':
let sumaFlecha = (a, b) => a + b;

// Llamada o invocación a las funciones:
let resultado = suma(2, 3);
alert(resultado);
